void solve() {
    ll n; cin>>n;
    vi a(2*n);
    read(a);
    vector<pair<ll,ll>> p;
    unordered_map<ll,ll> m;
    for(int i = 2*n-1; i>=0; i--)
    {
        if(m[a[i]] == 0)
        {
            m[a[i]] = i;
        }else{
            m[a[i]] = m[a[i]] - i;
            p.push_back({m[a[i]],a[i]});
        }
    }

    sort(all(p));

    for(int i = p.size()-1; i>=0; i--) cout<<p[i].second<<" ";
    cout<<"\n";
}

